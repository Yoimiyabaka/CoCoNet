class Triplet():

    def __init__(self):
        self.__s1 = 0
        self.__s2 = 0
        self.__s3 = 0

    def get_s1(self):
        return self.__s1

    def set_s1(self, s):
        self.__s1 = s

    def get_s2(self):
        return self.__s2

    def set_s2(self, s):
        self.__s2 = s

    def get_s3(self):
        return self.__s3

    def set_s3(self, s):
        self.__s3 = s
